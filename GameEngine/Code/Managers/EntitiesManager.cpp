#include "EntitiesManager.h"

namespace Engine
{
	//////////////////////////////////////////////////////////////////////////

	EntityID EntitiesManager::createEntity()
	{
		EntityID id = 0;
		for (EntityID takenId : m_takenIds)
		{
			if (takenId != id)
			{
				break;
			}
			id++;
		}

		m_takenIds.insert(id);
		return id;
	}

	//////////////////////////////////////////////////////////////////////////

	void EntitiesManager::destroyEntity(EntityID id)
	{
		m_takenIds.erase(id);
	}

	//////////////////////////////////////////////////////////////////////////

	void EntitiesManager::clear()
	{
		m_takenIds.clear();
	}

	//////////////////////////////////////////////////////////////////////////
}